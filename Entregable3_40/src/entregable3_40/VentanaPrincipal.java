    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        Calcular calcular = new Calcular();
        String serie_numeros, raices, cuadrados, cubos;
        
        serie_numeros = txtGrupodedatos.getText();
        raices = calcular.CalcularRaices(serie_numeros);
        cuadrados =calcular.CalcularCuadrados(serie_numeros);
        cubos = calcular.CalcularCubos(serie_numeros);
        
        txtRaiz.setText(raices);
        txtCuadrados.setText(cuadrados);
        txtCubos.setText(cubos);
    }//GEN-LAST:event_btnCalcularActionPerformed

