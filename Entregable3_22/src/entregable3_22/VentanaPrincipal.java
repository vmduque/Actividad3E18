    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Codigo del boton btnCalcular:
        String nombres;
        double salario_basico,horas_trabajadas,salario_mensual;
        
        nombres = txtNombres.getText();
        salario_basico = Double.parseDouble(txtSalariobasico.getText());
        horas_trabajadas = Double.parseDouble(txtHorastrabajadas.getText());
        salario_mensual = Empleado.calcular_salario(salario_basico, horas_trabajadas);
        
        if (salario_mensual > 450000){
            txtNombres2.setText(nombres);
            txtSalariomensual.setText(String.valueOf(salario_mensual));
        }
        else {
            txtNombres2.setText(nombres);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

